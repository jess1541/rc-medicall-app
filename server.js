async function seedDatabase() {
    try {
        const [rows] = await pool.query("SELECT COUNT(*) as count FROM doctors");
        if (rows[0].count === 0) {
            console.log("🌱 Base de datos vacía. Iniciando carga masiva...");
            const lines = MASTER_DATA_CSV.trim().split('\n').slice(1);
            const doctors = lines.map((line, index) => {
                const parts = line.match(/(".*?"|[^",\s]+)(?=\s*,|\s*$)/g);
                if (!parts || parts.length < 2) return null;
                const clean = (p) => p ? p.replace(/^"|"$/g, '').trim().toUpperCase() : '';
                return {
                    id: `master-${index}`,
                    executive: clean(parts[0]) || 'SIN ASIGNAR',
                    name: clean(parts[1]),
                    specialty: clean(parts[2]) || 'MEDICINA GENERAL',
                    subSpecialty: clean(parts[3]),
                    address: clean(parts[4]),
                    hospital: clean(parts[5]),
                    phone: clean(parts[8]),
                    email: clean(parts[9]),
                    category: clean(parts[13]) || 'MEDICO',
                    schedule: JSON.stringify([]),
                    visits: JSON.stringify([])
                };
            }).filter(Boolean);

            const query = "INSERT INTO doctors (id, executive, name, specialty, subSpecialty, address, hospital, phone, email, category, schedule, visits) VALUES ?";
            const values = doctors.map(d => [d.id, d.executive, d.name, d.specialty, d.subSpecialty, d.address, d.hospital, d.phone, d.email, d.category, d.schedule, d.visits]);
            await pool.query(query, [values]);
            console.log(`✅ ${doctors.length} registros cargados.`);
        }
    } catch (err) { console.error("❌ Error seed:", err.message); }
}

async function initDB() {
    let retries = 5;
    while (retries > 0) {
        try {
            const connParams = dbConfig.socketPath 
                ? { user: dbConfig.user, password: dbConfig.password, socketPath: dbConfig.socketPath }
                : { host: dbConfig.host, user: dbConfig.user, password: dbConfig.password, port: dbConfig.port };

            const connection = await mysql.createConnection(connParams);
            await connection.query(`CREATE DATABASE IF NOT EXISTS \`${dbConfig.database}\`;`);
            await connection.end();

            pool = mysql.createPool(dbConfig);
            await pool.query(`CREATE TABLE IF NOT EXISTS doctors (id VARCHAR(100) PRIMARY KEY, category VARCHAR(50) DEFAULT 'MEDICO', executive VARCHAR(100), name VARCHAR(255), specialty VARCHAR(255), subSpecialty VARCHAR(255), address TEXT, hospital VARCHAR(255), area VARCHAR(100), phone VARCHAR(50), email VARCHAR(150), floor VARCHAR(20), officeNumber VARCHAR(20), birthDate VARCHAR(20), cedula VARCHAR(50), classification VARCHAR(5), socialStyle VARCHAR(50), attitudinalSegment VARCHAR(100), importantNotes TEXT, isInsuranceDoctor BOOLEAN DEFAULT FALSE, visits JSON, schedule JSON, updatedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;`);
            await seedDatabase();
            console.log("✅ DB Online Puerto 8080");
            break;
        } catch (err) {
            console.error(`❌ Intento ${retries}:`, err.message);
            retries--;
            await new Promise(res => setTimeout(res, 5000));
        }
    }
}

